-- 1. String Functions
-- LEN(): Returns the length of a string.
-- CONCAT(): Concatenates two or more strings.
-- SUBSTRING(): Returns a part of a string.
-- CHARINDEX(): Finds the position of a substring in a string.
-- UPPER(): Converts a string to uppercase.
-- LOWER(): Converts a string to lowercase.

-- 2. Numeric Functions
-- SUM(): Returns the sum of values in a numeric column.
-- AVG(): Returns the average of values in a numeric column.
-- MAX(): Returns the maximum value in a column.
-- MIN(): Returns the minimum value in a column.
-- ABS(): Returns the absolute value of a number.

-- 3. Date and Time Functions
-- GETDATE(): Returns the current date and time.
-- DATEADD(): Adds a specified amount of time to a date.
-- DATEDIFF(): Calculates the difference between two dates.
-- YEAR(): Extracts the year from a date.
-- MONTH(): Extracts the month from a date.
-- DAY(): Extracts the day from a date.

-- 4. Conversion Functions
-- CAST(): Converts a data type into another data type.
-- CONVERT(): Converts a data type into another data type.

-- 5. Aggregation Functions
-- COUNT(): Returns the number of rows in a result set.
-- GROUP BY: Groups rows based on a specified column.
-- HAVING: Filters the result of an aggregate function.

-- 6. Logical Functions
-- CASE: Performs conditional operations.
-- IIF(): Returns one of two values depending on a condition.

-- 7. Mathematical Functions
-- ROUND(): Rounds a numeric value to a specified decimal place.
-- FLOOR(): Rounds a numeric value down to the nearest whole number.
-- CEILING(): Rounds a numeric value up to the nearest whole number.
-- SQUARE(): Returns the square of a number.
-- POWER(): Returns a number raised to a specified power.

-- 8. System Functions
-- @@VERSION: Returns the version of SQL Server.
-- @@ERROR: Returns the error code of the last executed statement.
-- @@ROWCOUNT: Returns the number of rows affected by the last statement.

-- 9. String Manipulation Functions
-- STUFF(): Replaces a specified portion of a string with another string.
-- REPLACE(): Replaces all occurrences of a substring in a string.
-- LTRIM(): Removes leading spaces from a string.
-- RTRIM(): Removes trailing spaces from a string.
